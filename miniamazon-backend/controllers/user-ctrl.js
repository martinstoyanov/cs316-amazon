loginUser = (req, res) => {
    const body = req.body

    if (!body) {
        return res.status(400).json({
            success: false,
            error: 'You must provide a body to login',
        })
    }

    User.findOne({ user_name: req.body.user_name, user_password: req.body.user_password }, (err, user) => {
        if (err || !user) {
            return res.status(404).json({
                err,
                message: 'User not found!',
            })
        }
    
        return res.status(200).json({
            success: true,
            id: user._id,
            message: 'User logged in!',
        })
    })
}

updateUser = async (req, res) => {
    const body = req.body

    if (!body) {
        return res.status(400).json({
            success: false,
            error: 'You must provide a body to update',
        })
    }

    User.findOne({ _id: req.body._id }, (err, user) => {
        if (err) {
            return res.status(404).json({
                err,
                message: 'User not found!',
            })
        }
        user.user_name = body.user_name
        user.username = body.username
        user.user_email = body.user_email
        user.user_password = body.user_password
        user.balance = body.balance
        user.orders = body.orders
        user
            .save()
            .then(() => {
                return res.status(200).json({
                    success: true,
                    id: user._id,
                    message: 'User updated!',
                })
            })
            .catch(error => {
                return res.status(404).json({
                    error,
                    message: 'User not updated!',
                })
            })
    })
}

